# Tampilan variabel dengan tipe data desimal, octal dan hexadecimal
angka = 15
print("angka Desimal:", angka, "-", type(angka))
print("angka Octal:", oct(angka), "-", type(oct(angka)))
print("angka Hexadesimal:", hex(angka), "-", type(hex(angka)))

print("----------------------------------------------------------")